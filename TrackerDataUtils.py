# Helper function to convert Excel column letters (e.g., 'A', 'AA') to a 0-indexed number
def excel_column_to_number(column_name):
    number = 0
    for char in column_name:
        number = number * 26 + (ord(char.upper()) - ord('A')) + 1
    return number - 1 # Convert to 0-indexed


def create_update_request(cell_a1_notation, tracker_data, tracker_number=None, angle=None):
    """Creates a Google Sheet update request dictionary for a single cell."""
    is_stuck = (tracker_data.max() - tracker_data.min()) <= 5
    is_comms_lost = tracker_data.apply(lambda x: pd.isna(x) or x == '').all()

    if tracker_number is not None and isinstance(tracker_number, str):
        prefix = tracker_number
    else:
        prefix = f"Row {tracker_number}"


    if is_stuck or is_comms_lost:
        # Red color for "Stuck"
        color = {"red": 1.0, "green": 0.0, "blue": 0.0}
        if tracker_number and angle is not None:
            cell_text = f"{prefix}: {round(angle, 1)}°"
        elif tracker_number and is_comms_lost:
            cell_text = f"{prefix}: Comms"
        else:
            cell_text = f"{prefix}"
    else:
        # Green color for "OK"
        color = {"red": 0.0, "green": 1.0, "blue": 0.0}
        cell_text = f"Row {tracker_number}"

    # The sheetId is hardcoded to 0, assuming the target is always the first sheet.
    sheet_id = 0
    start_row = int(re.search(r'\d+', cell_a1_notation).group()) - 1
    column_letters = re.search(r'[A-Z]+', cell_a1_notation).group().upper()
    start_col = excel_column_to_number(column_letters)

    return {
        "updateCells": {
            "range": {
                "sheetId": sheet_id,
                "startRowIndex": start_row,
                "endRowIndex": start_row + 1,
                "startColumnIndex": start_col,
                "endColumnIndex": start_col + 1
            },
            "rows": [{
                "values": [{"userEnteredValue": {"stringValue": cell_text},
                            "userEnteredFormat": {"backgroundColor": color, "horizontalAlignment": "CENTER"}}]
            }],
            "fields": "userEnteredValue,userEnteredFormat(backgroundColor,horizontalAlignment)"
        }
    }




